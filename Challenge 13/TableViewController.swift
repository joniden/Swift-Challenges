    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("contentCell", forIndexPath: indexPath) as! TableViewCell
        
        tableView.rowHeight = 150
        
        //Define the row
        let row = data[indexPath.row]!
        
        //Row image
        let image = UIImage(named:row["image"] as! String)
        
        cell.backgroundView = UIImageView(image: image)
        cell.backgroundColor = UIColor.clearColor()
        cell.cellTitle.text = row["title"] as? String
        cell.cellTitle.backgroundColor = UIColor(white: 1, alpha: 0.5)
        
        cell.cellBody.text = row["body"] as? String
        cell.cellBody.backgroundColor = UIColor(white: 1, alpha: 0.5)
        
        
        

        return cell
    }


